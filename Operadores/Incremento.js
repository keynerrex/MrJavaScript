  b = 2;
let resultado = a + b;

//Incremento

//Pre-Incremeneto (el operador ++ antes de la variable)

resultado = ++a; // 3 suma 1 -> 4
console.log(a);
console.log(resultado);

//Post-Incremento(el operador ++ despues de la variable)

resultado = b++;
console.log(b); //Incrementa
console.log(resultado); //Y este aggara el valor de b
